    <h2>Opciones de envio</h2>

    <ul>
        <li>
            <h3>¿Dónde puedo recibir mi pedido?</h3>
            <p>En tu domicilio, trabajo o sucursal de correo OCA habilitada.</p>
        </li>

        <li>
            <h3>¿Cuál es el costo del envío?</h3>
            <p>El envío es gratuito a todo el país en compras mayores a $3900.Para compras menores a $3900 podrás encontrar el valor del envío en la etapa previa a confirmar tu compra. El valor se encuentra sujeto a la zona de entrega y peso del paquete.</p>
        </li>

        <li>
            <h3>¿Cuánto tardará en llegar mi pedido?</h3>
            <p>Nuestros envíos a Capital Federal y Gran Buenos Aires a través de Ship Now demoran de 2 a 5 días hábiles, una vez despachado tu pedido. Resto del país: 5-10 días hábiles. Tierra del Fuego 15 días hábiles. En eventos masivos tales como Hot Sale o Cyber Monday este período puede extenderse hasta 5 días adicionales de demora.</p>
        </li>

        <li>
            <h3>¿Cómo sé que lo voy a recibir?</h3>
            <p>Cuando Ship Now tenga tu pedido en su depósito para hacer la entrega vas a recibir en tu correo notificaciones ante cada movimiento del pedido. Estas notificaciones se envían a nombre de Ship Now. Te recomendamos prestar atención a tu bandeja de entrada. En el caso de envíos por motomensajería sólo nos comunicaremos con vos en el caso de presentarse algún inconveniente para realizar la entrega.</p>
        </li>

        <li>
            <h3>¿Qué pasa si puse mal la dirección?</h3>
            <p>Si te diste cuenta apenas realizaste la compra, escribinos a hola@pura.com.ar para que podamos solucionarlo a la brevedad.</p>
        </li>

        <li>
            <h3>¿Qué pasa si viene el correo y no estoy para recibirlo?</h3>
            <p>Cualquier persona mayor de 18 años puede recibir el paquete a tu nombre. Si recibiste una visita en una entrega de Ship Now y no encontraron a nadie en el domicilio, el correo vuelve a pasar una vez más para poder entregar tu compra.</p>
        </li>

        <li>
            <h3>¿Puedo pautar un horario para que pase el correo?</h3>
            <p>Ship Now realiza entregas de 9:00 a 18:00 hs en días hábiles. No es posible coordinar un horario exacto o acotar el rango horario de entrega. Si no tenés una dirección donde el correo pueda encontrar a alguien, nuestra recomendación es que elijas el envío a sucursal OCA para poder retirarlo en persona según tu disponibilidad horaria.</p>
        </li>

        <li>
            <h3>¿Qué pasa si no retiro mi pedido a tiempo de la sucursal?</h3>
            <p>Si no pudiste pasar por la sucursal del correo para retirar a tiempo podemos coordinar un nuevo envío a tu domicilio. Este nuevo envío tiene costo.</p>
        </li>

        <li>
            <h3>¿Puedo pasar a retirar mi pedido?</h3>
            <p>Si estás en Corrientes podés seleccionar retiro por nuestro depósito ubicado dentro del Parque Industrial de Haedo.
            <br>Podés retirar a las 24 hs hábiles de haber realizado la compra.<br>Estamos en General Paz, Corrientes. De Lunes a Viernes de 8 a 13 hs y de 17 a 21. </p>
        </li>

    </ul>
